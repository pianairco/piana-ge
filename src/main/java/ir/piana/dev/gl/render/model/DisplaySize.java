package ir.piana.dev.gl.render.model;

public class DisplaySize {
    private int width;
    private int height;

    public DisplaySize(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
